print("Пути: налево(коня), направо(себя), прямо(всё), назад(без славы)")
choice = input("Твой выбор? ").strip().lower()

if choice in ["налево", "л"]: print("Коня потерял, сам жив")
elif choice in ["направо", "п"]: print("Сам погиб, конь спасся")
elif choice in ["прямо", "пр"]: print("Погибли оба")
elif choice in ["назад", "н"]: print("Жив, но без славы")
else: print("Заблудился и сгинул")